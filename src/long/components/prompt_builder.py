class PromptCache:
    """提示词缓存管理"""

    def __init__(self):
        self._static_prompt: str = ""
        self._cached: str = ""
        self._dirty: bool = True

    def set_static(self, prompt: str) -> None:
        """设置静态提示词（会话期间不变）"""
        self._static_prompt = prompt
        self._dirty = True

    def mark_dirty(self) -> None:
        """标记缓存为脏，下次 build 时重新构建"""
        self._dirty = True

    def build(self, semistatic_builder, dynamic_time: str = "") -> str:
        """构建完整提示词，支持缓存"""
        if not self._dirty and self._cached:
            return self._cached

        semistatic = semistatic_builder()
        parts = [self._static_prompt, semistatic]
        if dynamic_time:
            parts.append(dynamic_time)
        self._cached = "\n\n".join(parts)
        self._dirty = False
        return self._cached


